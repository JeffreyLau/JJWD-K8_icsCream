static int write_regs_mt9t111(const struct sensor_reg reglist[])
{
	int err;
	int err_cnt = 0;	
	int sleep_cnt = 100;
	unsigned char data[132];
	unsigned char bytes;
	const struct sensor_reg *next = reglist;

	while (!((next->reg == MT9T111_REG_TERM) && (next->val == MT9T111_VAL_TERM)))
	{
		
		if(next->reg == MT9T111_REG_TERM && next->val != MT9T111_VAL_TERM)
		{
			//mdelay(next->val);
			msleep(next->val);
			sleep_cnt = 100;
			printk("Sensor init Delay[%d]!!!! \n", next->val);
			next++;
		}
		else
		{
			sleep_cnt--;
			if(sleep_cnt == 0)
			{
				msleep(10);
				sleep_cnt = 100;
			}

			if(next->width == WORD_LEN){			
				bytes = 0;
				data[bytes]= next->reg>>8;		bytes++;		
				data[bytes]= (u8)next->reg&0xff; 	bytes++;

				data[bytes]= next->val>>8;		bytes++;		
				data[bytes]= (u8)next->val&0xff; 	bytes++;

				err = DDI_I2C_Write(data, 2, bytes-2);			
			}
			else{
				bytes = 0;
				data[bytes]= next->reg>>8;		bytes++;		
				data[bytes]= (u8)next->reg&0xff; 	bytes++;

				//data[bytes]= next->val;		bytes++;		
				data[bytes]= (u8)next->val&0xff; 	bytes++;

				err = DDI_I2C_Write(data, 2, bytes-2);				
			}
			
			
			if (err)
			{
				err_cnt++;
				if(err_cnt >= 3)
				{
					printk("ERROR: Sensor I2C !!!! \n"); 
					return err;
				}
			}
			else
			{
				err_cnt = 0;
				next++;
			}
		}
	}

	return 0;
}

